class Entry:
    def __init__(self, c):
        self.c = c
        self.cost = 0
        self.height = ord(c) - ord("a") + 1 if c not in ["S", "E"] else (1 if c == "S" else 26)


def get_map(i):
    i = i.split("\n")
    m = [[] for _ in range(len(i))]

    for r in range(len(i)):
        for c in range(len(i[0])):
            m[r].append(Entry(i[r][c]))
    return m


def solve(i, part):
    m = get_map(i)

    def bfs(part):
        queue = []
        seen = []

        for r in range(len(m)):
            for c in range(len(m[0])):
                if m[r][c].c == "S" or (part == 2 and m[r][c].height == 1):
                    queue.append([c, r])

        while queue:
            point = queue.pop(0)
            x, y = point
            if point in seen:
                continue
            seen.append(point)
            if m[y][x].c == "E":
                return m[y][x].cost

            for way in [[-1, 0], [1, 0], [0, -1], [0, 1]]:
                dx, dy = way
                if 0 <= x + dx <= len(m[1]) - 1 and 0 <= y + dy <= len(m) - 1:
                    if m[y + dy][x + dx].height <= m[y][x].height + 1 and [x + dx, y + dy] not in queue:
                        m[y + dy][x + dx].cost = m[y][x].cost + 1
                        queue.append([x + dx, y + dy])

    return bfs(part)


